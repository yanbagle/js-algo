const characterReplacement = (s, k) => {
  const count = {};
  let res = 0;
  let l = 0;
  let r = 0;
  
  while (r < s.length) {
    const c = s.charAt(r);
    count[c] = count[c] ? count[c] + 1 : 1;
    
    const windowLength = r - l + 1;
    const mostFreq = Math.max(...Object.values(count));
    if (windowLength - mostFreq > k) {
      const lChar = s.charAt(l);
      count[lChar] -= 1;
      l++;
    }
    
    res = Math.max(res, r - l + 1);
    r++;
  }
  
  return res;
}

