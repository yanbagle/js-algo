const characterReplacement = (s, k) => {
  let longestWindow = 0;
  let l,
    r = 0;
  const freqTable = {};

  while (r < s.length) {
    const c = s[l];
    freqTable[c] = freqTable[c] ? freqTable[c] + 1 : 1;

    const window = r - l + 1;
    if (window - Math.max(...Object.values(freqTable)) > k) {
      freqTable[c] = freqTable[c] - 1;
      l++;
    }

    longestWindow = Math.max(longestWindow, r - l + 1);
    r++;
  }
  return longestWindow;
};

