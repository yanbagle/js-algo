/*

Given the root of a binary tree, return the level order traversal of its nodes' values. (i.e., from left to right, level by level).

https://leetcode.com/problems/binary-tree-level-order-traversal/

Input: root = [3,9,20,null,null,15,7]
Output: [[3],[9,20],[15,7]]

*/

/*

add root into queue

while queue is not empty
  level = []
  for loop through queue 
    n = q.shift 
    add n.val into level array
    add n's left and right child into queue
  if level is not empty
    push level into res array
    
return res
*/

const levelOrder = (root) => {
  
  const q = [root];
  const res = [];
  
  while (q.length) {
    const len = q.length;
    const level = [];
    for (let i = 0; i < q.length; i++) {
      const n = q.shift();
      if (n) {
        level.push(n);
        q.push(n.left);
        q.push(n.right);
      }
    }
    let total = 0;
    for (let i = 0; i < level.length; i++) {
      total += level[i];
    }
    res.push(Math.ceiling(total / level.length));
  }
  
  return res;
}

